package UdemyCourse;

public class IfElseIf {
    public static void main(String[] args) {
        int hi = 6;
        if (hi > 0) {
            System.out.println("Positive number :)");
        } else if (hi < 0) {
            System.out.println("Negative number ):");
        } else {
            System.out.println("ZERO !");
        }
    }
}
